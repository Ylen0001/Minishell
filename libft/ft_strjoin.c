#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	size_t	i;
	size_t	j;
	size_t	bigfat_len;
	char	*new;

	i = 0;
	j = 0;
	bigfat_len = ft_strlen(s1) + ft_strlen(s2);
	new = (char *)malloc(bigfat_len + 1);
	if (!new)
		return (NULL);
	while (s1[i])
	{
		new[i] = s1[i];
		i++;
	}
	while (s2[j])
	{
		new[i] = s2[j];
		i++;
		j++;
	}
	new[i] = 0;
	return (new);
}
